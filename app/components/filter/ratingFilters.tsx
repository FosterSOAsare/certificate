const StarRow = ({ rating }: { rating: number }) => {
  let halfCount = Number.isInteger(rating) ? 0 : 1;
  const fullCount = Math.floor(rating);
  const emptyCount = 5 - halfCount - Math.floor(rating);

  const listEmpty = Array.from({ length: emptyCount }).map((_, i: number) => (
    <EmptyStar key = {i} />
  ));

  const listHalf = Array.from({ length: halfCount }).map((_, i: number) => (
    <HalfStar key = {i} />
  ));
  const listFull = Array.from({ length: fullCount }).map((_, i: number) => (
    <FullStar key = {i} />
  ));

  return (
    <span className="grid grid-cols-5 items-center justify-start ">
      {listFull}
      {listHalf}
      {listEmpty}
    </span>
  );
};

